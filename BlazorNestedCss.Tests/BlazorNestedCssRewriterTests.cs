    [Fact]
    public void When_Deep_Complex_Selectors_CssRewrite_Ignores_Scope()
    {
        var cssText = @"
.foo:has(.sel1)::after ::deep .bar:has(sel2)::after { }
.foo {
    ::deep .bar { table { th.tagme { &.account-name { } } } }
    .bar { ::deep table { th.tagme { &.account-name { } } } }
    .bar { table { th.tagme { &.account-name { } } } } 
}
";
        var expected = @"
.foo[b-scope]:has(.sel1[b-scope])::after .bar:has(sel2)::after { }
.foo[b-scope] {
    .bar { table { th.tagme { &.account-name { } } } }
    .bar[b-scope] { table { th.tagme { &.account-name { } } } }
    .bar[b-scope] { table[b-scope] { th.tagme[b-scope] { &.account-name { } } } } 
}
";

        var root = _parser.Parse(cssText);
        new BlazorScopeRewriter().Rewrite(root, "b-scope");
        var output = _parser.Generate(root);

        Assert.Equal(prefix + expected, output);
    }

