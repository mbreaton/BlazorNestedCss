.foo[b-scope]/* this is a selector comment .bar */ { }
.foo[b-scope]/* this is a selector comment */.bar[b-scope]/* this is a selector comment */.bar[b-scope] { }
.foo[b-scope]/* this is a selector comment */.bar[b-scope] .bar[b-scope] { }
