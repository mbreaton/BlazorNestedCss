    [Fact]
    public void When_Simple_Block_Selectors_With_PseudoClass_CssRewrite_Adds_Scope()
    {
        var cssText = @"
.foo:nth-child(2n+1) { }
.foo:has(.sel:not(.bar)) { }
.foo:has(.sel:not(.bar /* */)) { }
";
        // tbody > tr.row:not(:has(td.error)):nth - child(4n + 2 of: not(.hidden)):hover { }
        var expected = @"
.foo[b-scope]:nth-child(2n+1) { }
.foo[b-scope]:has(.sel[b-scope]:not(.bar[b-scope])) { }
.foo[b-scope]:has(.sel[b-scope]:not(.bar[b-scope] /* */)) { }
";

        var root = _parser.Parse(cssText);
        new BlazorScopeRewriter().Rewrite(root, "b-scope");
        var output = _parser.Generate(root);

        Assert.Equal(prefix + expected, output);
    }

