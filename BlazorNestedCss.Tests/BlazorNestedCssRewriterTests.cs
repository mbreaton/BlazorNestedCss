namespace BlazorNestedCss.Tests;

using BlazorNestedCss.Tasks;

public class BlazorNestedCssRewriterTests
{
    CssParser _parser;
    string prefix;

    public BlazorNestedCssRewriterTests()
    {
        _parser = new CssParser();

        prefix = $"/* CSS Generated by {string.Join(",", _parser.GetType().Assembly.FullName!.Split(',').Take(2))} */\r\n";
    }

    [Fact]
    public void When_Simple_Block_Selectors_CssRewrite_Adds_Scope()
    {
        var cssText = @"
.foo { .bar1, .bar2 { } }
";
        var expected = @"
.foo[b-scope] { .bar1[b-scope], .bar2[b-scope] { } }
";
        var root = _parser.Parse(cssText);
        new BlazorScopeRewriter().Rewrite(root, "b-scope");
        var output = _parser.Generate(root);

        Assert.Equal(prefix + expected, output);
    }

    [Fact]
    public void When_Simple_Block_Selectors_With_Attrs_CssRewrite_Adds_Scope()
    {
        var cssText = @"
.foo[attr] { .bar[attr] [attr] { } }
